package polymorphism_Overloading;

public class MethodOverloding {

	public int add(int a) {
		int sum = a+1;
		
		return sum;
		
		
	}
	
	public double add(double a, double b) {
		
		
		return a+b;
		
		
	}
	
	public double add(int a, double b) {
		
		
		return a+b;
		
		
	}
	public double add(double a,double b, double c) {
		
		
		return a+b+c;
		
		
	}
	
	
	
	
	
	
}
